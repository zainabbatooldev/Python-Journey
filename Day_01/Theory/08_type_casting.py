# process of converting a variable from one data type to another..

# str to int type casting

age = "20"
age= int(age)
print(age)
print(type(age))

# int to float 

num=19
num=float(num)
print(num)
print(type(num))

#int to str

number=19
number=str(number)
print(number)
print(type(number))

# number to boolean

x = bool(1)
y = bool(0)
print(x)
print(y)


# type casting with input

